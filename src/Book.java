public class Book {

   private String name1;
   private int age1;

    public Book(String name1, int age1) {
        this.name1 = name1;
        this.age1 = age1;
    }

    public String getName1() {
        return this.name1;
    }
    public int getAge1() {
        return this.age1;
    }
    public void setAge1(int age1){
        this.age1=age1;
    }


}